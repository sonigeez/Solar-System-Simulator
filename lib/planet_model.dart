import 'dart:ui';

class Planet {
  final String name;
  final Color color;
  final double distanceFromSun;
  final double radius;
  final double orbitalDuration;

  Planet({
    required this.name,
    required this.color,
    required this.distanceFromSun,
    required this.radius,
    required this.orbitalDuration,
  });
}
